uint8_t AsciToint (const char v_asci)
{
    if (v_asci >= '0' && v_asci <= '9')
        return v_asci - '0';
    if (v_asci >= 'A' && v_asci <= 'F')
        return v_asci - 'A' + 10;
    if (v_asci >= 'a' && v_asci <= 'f')
        return v_asci - 'a' + 10;
    return -1;
}

uint8_t AsciToInt (const char v_asciTwoBytesTabInHex [TWO])
{
    return (AsciToint (v_asciTwoBytesTabInHex [FIRST_BYTE]) << FOUR_BITS) + AsciToint (v_asciTwoBytesTabInHex [SECOND_BYTE]);
}

