uint8_t CharToint (const char v_char)
{
    if (v_char >= '0' && v_char <= '9')
        return v_char - '0';
    if (v_char >= 'A' && v_char <= 'F')
        return v_char - 'A' + 10;
    if (v_char >= 'a' && v_char <= 'f')
        return v_char - 'a' + 10;
    return -1;
}

uint8_t HexToInt (const char v_char [TWO])
{
    return (CharToint (v_char [FIRST_BYTE]) << FOUR_BITS) + CharToint (v_char [SECOND_BYTE]);
}

